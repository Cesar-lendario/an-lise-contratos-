// Fun√ß√£o para extrair texto dos diferentes tipos de arquivos
async function extractTextFromFile(filePath, fileType) {
  try {
    console.log(`Extraindo texto de: ${filePath} (${fileType})`);
    
    // Para arquivos de texto simples (TXT)
    if (fileType === 'text/plain') {
      return fs.readFileSync(filePath, 'utf8');
    }
    
    // Para arquivos PDF - usando um m√©todo alternativo para evitar problemas com pdf-parse
    else if (fileType === 'application/pdf') {
      try {
        console.log(`Processando PDF: ${filePath}`);
        
        // Ler o arquivo PDF
        const pdfContent = fs.readFileSync(filePath);
        
        // Usar m√©todo alternativo (texto simulado por enquanto)
        console.log(`PDF recebido com ${pdfContent.length} bytes.`);
        
        // Para fins de teste, retornar o texto do pr√≥prio arquivo
        // Em produ√ß√£o, usar uma biblioteca mais robusta
        return `Conte√∫do do PDF: ${path.basename(filePath)} (${pdfContent.length} bytes)\n\n` + 
               fs.readFileSync(filePath).toString('utf8', 0, 500) + '...';
      } catch (pdfError) {
        console.error('Erro ao processar o PDF:', pdfError);
        throw new Error('N√£o foi poss√≠vel extrair texto do arquivo PDF.');
      }
    }
    
    // Para arquivos DOCX
    else if (fileType === 'application/vnd.openxmlformats-officedocument.wordprocessingml.document') {
      try {
        const docxBuffer = fs.readFileSync(filePath);
        const result = await mammoth.extractRawText({ buffer: docxBuffer });
        return result.value;
      } catch (docxError) {
        console.error('Erro ao extrair texto do DOCX:', docxError);
        throw new Error('N√£o foi poss√≠vel extrair texto do arquivo DOCX.');
      }
    }
    
    throw new Error('Formato de arquivo n√£o suportado.');
  } catch (error) {
    console.error('Erro na extra√ß√£o de texto:', error);
    throw error;
  }
}

